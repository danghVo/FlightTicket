    public static void showFlightPanel(ResultSet flights){
        try{
            flightArr = new ArrayList<flightPanel>();
            jScrollPane1.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_AS_NEEDED);
            panelcontain.removeAll();
            int sumHeight = 0;
            if(!flights.next()) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy  nơi đi nơi đến bạn yêu cầu");
                return;
            }
            else do{
                String from = flights.getString("from");
                String to = flights.getString("to");
                String date = flights.getString("date");
                String time = flights.getString("boarding_time");
                String flight = flights.getString("flight");
                String seat = flights.getInt("seat") + "";
                String gate = flights.getString("gate");
                int id = flights.getInt("id");
                flightArr.add(new flightPanel(new Flight(from, to, date, time, Integer.parseInt(seat), gate, flight, id)));
                flightPanel temp = flightArr.get(flightArr.size() - 1);
                temp.setSize(new Dimension(600, 170));
                sumHeight += 170;
                panelcontain.add(temp);
                }   while(flights.next());
            int scrollHeight = panelcontain.getHeight();
                if(sumHeight < scrollHeight) {
                    JPanel addSpace = new JPanel();
                    addSpace.setSize(600, scrollHeight - sumHeight - 200);
                    panelcontain.add(addSpace);   
            }
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
    }
    
    
