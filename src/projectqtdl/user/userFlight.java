    public static void showFrom(ResultSet froms){
        try{
            fromFilter.removeAll();
            fromFilter.addItem("--");
            int count = 1;
            while(froms.next()) {
                fromFilter.addItem(froms.getString(count));
            }
        }
        catch(SQLException ex) {
            ex.printStackTrace();
        }
    }
    
