        // TODO add your handling code here:
        String[] srcIcon = this.disable.getIcon().toString().split("/");
        String closeEye = "external-eyes-neu-interface-neu-royyan-wijaya-2.png";
        String openEye = "external-eyes-neu-interface-neu-royyan-wijaya.png";
        if(srcIcon[srcIcon.length - 1].equals(closeEye)){
            this.disable.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/" + openEye)));
            this.txtpassword.setEchoChar((char) 0);
        }
        else{
            this.disable.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/" + closeEye)));
            this.txtpassword.setEchoChar('*');
        } 
        
