    public void logIn() {
        String username = txtusername.getText();
            String password = new String(txtpassword.getPassword());

            if(username .equals("") || password.equals("")){
                JOptionPane.showMessageDialog(rootPane,"username và password không được để trống");  
                return;
            }

            if(UserModel.logIn(username, password)){
                if(username.equals("admin")){
                    new adminFlight().setVisible(true);
                }
                else new userFlight(UserModel.getUserId(username)).setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "username hoặc password sai");
            }
    }

