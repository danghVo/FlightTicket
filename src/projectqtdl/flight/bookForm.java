    private void bookBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bookBtnMouseClicked
        // TODO add your handling code here:
        String seat =(String) this.seatSelected.getSelectedItem();
        int payed = 0;
        if(this.payOption == 1) {
            Object[] options =  {"Xong", "Thanh toán sau"};
            ImageIcon icon = new ImageIcon(getClass().getResource("/icon/qr.png"));
            int n =JOptionPane.showOptionDialog(rootPane,
                null,
                "Hãy quét mã để thanh toán", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.INFORMATION_MESSAGE,icon, 
                options, 
                options[0]);
            System.out.print(JOptionPane.YES_OPTION);
            if(JOptionPane.YES_OPTION == n) {
                 payed = 1;
            }
        }
        else JOptionPane.showMessageDialog(rootPane, "Đến trước 1 tiếng để thanh toán");
        if(UserModel.bookTicket(flight.getId(), String.valueOf(seat.charAt(0)),Character.getNumericValue(seat.charAt(1)), this.payOption - 1, payed,this.userId)){
            userFlight.defaultJPane();
            dispose();
            return;
        }
        else JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra. Hãy thử lại");
        
    }//GEN-LAST:event_bookBtnMouseClicked

