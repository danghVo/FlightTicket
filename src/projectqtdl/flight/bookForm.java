    public void showInfor() {
        try{
            this.fromField.setText(flight.from);
            this.toField.setText(flight.to);
            this.dateField.setText(flight.date);
            this.timeField.setText(flight.boardingTime);
            this.payFormField.setText(this.payOption==1? "Thanh toán online":"Thanh toán trực tiếp");
            ResultSet ticket = flightModel.getAllTicketOf(flight.getId());
            while(ticket.next()) {
                if(ticket.getInt("user_id") == 0)
                    this.seatSelected.addItem(ticket.getString("seat_name") + ticket.getString("seat_number"));
            }
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
        
    }

