    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String from = this.fromField.getText();
        String to = this.toField.getText();
        String date = this.dayField.getText() + "-" + 
                      this.monthField.getText() + "-" + 
                      this.yearField.getText();
        String minute = this.minuteField.getText().equals("")? "00":this.minuteField.getText();
        String second = this.secondField.getText().equals("")? "00":this.secondField.getText();
        String boardingTime = this.hourField.getText() + ":" + 
                              minute + ":" + 
                              second;
        String gate = this.gateField.getText();
        String seat = this.seatField.getText();
        String flight = this.flightField.getText();
        
        String message = checkFlight.check_valid(from, to, date, boardingTime, seat, gate, flight);
        if(message != "") JOptionPane.showMessageDialog(null, message);
        else if(this.state == 1){
                if(flightModel.addFlight(new Flight(from, to, date, boardingTime, Integer.parseInt(seat), gate, flight, this.id))){
                     adminFlight.resetShowFlight();                    
                     adminFlight.showFrom(flightModel.getAllFrom());
                     adminFlight.showTo(flightModel.getAllTo());
                     dispose();
                }
                else JOptionPane.showMessageDialog(null, "Có lỗi xảy ra. Hãy thử lại sau");
            }
        else if(this.state == 2){
                if(flightModel.updateFlight(new Flight(from, to, date, boardingTime, Integer.parseInt(seat), gate, flight, this.id))){
                     adminFlight.resetShowFlight();
                     adminFlight.showFrom(flightModel.getAllFrom());
                     adminFlight.showTo(flightModel.getAllTo());
                    dispose();
                }
                else JOptionPane.showMessageDialog(null, "Có lỗi xảy ra. Hãy thử lại sau");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

