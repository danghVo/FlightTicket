    public void addFlightValue() {
       Flight ticket = Flight.getFlightById(this.id);
       this.fromField.setText(ticket.from);
       this.toField.setText(ticket.to);
       this.seatField.setText(ticket.seat + "");
       this.gateField.setText(ticket.gate);
       this.flightField.setText(ticket.flight);
       String[] arrOfDate = ticket.date.split("-");
       String[] arrOfTime = ticket.boardingTime.split(":");
       
       this.dayField.setText(arrOfDate[0]);
       this.monthField.setText(arrOfDate[1]);
       this.yearField.setText(arrOfDate[2]);
       this.hourField.setText(arrOfTime[0]);
       this.minuteField.setText(arrOfTime[1]);
       this.secondField.setText(arrOfTime[2]);
        
    }
    
